Kim Hyun-ah (lahir 6 Juni 1992), lebih dikenal dengan mononim Hyuna, juga ditulis sebagai HyunA, adalah penyanyi, penari, penulis lagu dan model Korea Selatan. Dia menjadi terkenal pada akhir tahun 2000-an sebagai anggota dari Wonder Girls, meninggalkan grup tak lama setelah debut pada tahun 2007 dan kemudian bergabung dengan 4Minute pada tahun 2009.[1] Hyuna memulai karier solo pada tahun 2010, yang ia gambarkan sebagai "performance-oriented music".[1] Singel debutnya "Change" memuncak pada posisi kedua di Gaon Digital Chart.

Ia merilis album mini pertamanya Bubble Pop! pada tahun 2011. Walaupun album mininya tidak terjual sedikit maupun banyak, singel utamanya terjual lebih dari dua setengah juta kopi dan Hyuna menjadi solo wanita K-pop pertama yang mendapatkan 100 juta penonton dalam satu video YouTube. Pada tahun 2012, ia tampil dalam lagu hit dunia milik Psy "Gangnam Style", yang juga menjadi duet sukses. Melting (2012) adalah album mini pertama Hyuna yang menduduki posisi pertama di Gaon chart dengan singel "Ice Cream". Menyusul bubarnya 4Minute pada tahun 2016, ia merilis album mini kelimanya A'wesome yang didukung oleh tur konser pertamanya di Amerika Serikat.

Hyuna telah berkolaborasi dengan beberapa artis Cube Entertainment, pertama membentuk duo Trouble Maker dengan Jang Hyun-seung pada tahun 2011; yang melahirkan dua single nomor satu. Pada tahun 2017, agensi mengumumkan keterlibatannya dalam trio Triple H.

